package com.viptrip.wetrip.model.policy;

public class PolicyValueModel {
	private String recKey;
	private Double topPrice;
	private String topLv;
	public String getRecKey() {
		return recKey;
	}
	public void setRecKey(String recKey) {
		this.recKey = recKey;
	}
	public Double getTopPrice() {
		return topPrice;
	}
	public void setTopPrice(Double topPrice) {
		this.topPrice = topPrice;
	}
	public String getTopLv() {
		return topLv;
	}
	public void setTopLv(String topLv) {
		this.topLv = topLv;
	}
	
}
