package com.viptrip.wetrip.vo;

public class BookJson {
	public String mapKey;
	public Integer type;
	public Long timestamp;
	public String excessinfo;
	public String selectusers;
	
	public String getMapKey() {
		return mapKey;
	}
	public void setMapKey(String mapKey) {
		this.mapKey = mapKey;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public Long getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(Long timestamp) {
		this.timestamp = timestamp;
	}
    public String getExcessinfo() {
        return excessinfo;
    }
    public void setExcessinfo(String excessinfo) {
        this.excessinfo = excessinfo;
    }
    public String getSelectusers() {
        return selectusers;
    }
    public void setSelectusers(String selectusers) {
        this.selectusers = selectusers;
    }
	
}
